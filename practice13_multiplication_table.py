def print_table(n):
    for i in range(1,6):
        print(f"{n} * {i} = {n*i}")

def main():
    print("----------")
    print_table(2)
    print("----------")
    print_table(5)
    print("----------")
    print_table(13)
    print("----------")
    print_table(17)
    print("----------")


main()


